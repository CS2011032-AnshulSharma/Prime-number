class IOtranferObject:
    def __init__(self):
        self.inBuffer=StringIO("")
        self.outBuffer=StringIO("")
    def input():
        return self.inBuffer
    def check(o):
        if (self.inBuffer==o.inBuffer) and (self.outBuffer==o.outBuffer):
            return True
        else :
            return False

    